pub fn translate_byte(byte: u8) -> &'static str {
    CHAR_MAP[usize::from(byte)]
}

pub fn translate_bytes(bytes: &[u8]) -> String {
    bytes
        .iter()
        .flat_map(|c| translate_byte(*c).chars())
        .collect()
}

